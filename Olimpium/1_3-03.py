# целое положите число
int_1 = 5
print(int_1)

# целове отрицат в двоичной системе
int_2 = -0b11001
print((int_2))

# целое отрицат в 8 системе
int_3 = -0o35
print((int_3))

# целое положит в 16ти ричной СС
int_4 = 0xFE
print(int_4)

# Результат все равно отобразится в 10-й СС
res = int_1 + int_2 + int_3 + int_4
print(res)