				points = skills["Сила"] + skills["Здоровье"] + skills["Мудрость"] + skills["Ловкость"]
					
				print("\nВсего очков: ", points)
				print("Мудрость", skills.get("Мудрость"))
				print("\nТекущее распределение характеристик героя: ")
