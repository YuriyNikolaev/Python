# Программа должна предоставлять пользователю 30 пунктов, которые надо распределить по
# четырем характеристикам: Сила, Здоровье, Мудрость, Ловкость.
# Надо сделать так, чтобы пользователь мог не только брать эти пункты из общего «Пупа», но и­
# возвращать их туда из характеристик, которым он решит присвоить другие значения.


skills = {"Сила" : 8 , "Здоровье" : 8 , "Мудрость" : 7 , "Ловкость" : 7 }

#points = skills["Сила"] + skills["Здоровье"] + skills["Мудрость"] + skills["Ловкость"]

choice = None
choice1 = None
while choice != "0":
	print(
		"""
		Распределение очков у героя:
		0 - выйти из программы
		1 - просмотреть характеристики
		2 - изменить характеристика
		""")
	choice = input("Ваш выбор: ")

