				print("\nВсего очков: ", points)
				print("Сила", skills.get("Сила"))
				print("\nТекущее распределение характеристик героя: ")
				for skill in skills:
					print(skill, skills.get(skill))		
				print("\nВсего распределено ", points, "очков 'Силы' из 30ти.")

