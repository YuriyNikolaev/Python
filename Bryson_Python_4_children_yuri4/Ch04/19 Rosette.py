import turtle
t = turtle.Pen()
t.circle(100) # Отрисовка первой окружности (показывает
              # на север)
t.left(90) # затем черепашка поворачивает налево
           # на 90 градусов
t.circle(100) # Отрисовка второй окружности (показывает
              # на запад)
t.left (90)  # затем черепашка поворачивает налево
           # на 90 градусов
t.circle(100) # Отрисовка третей окружности (показывает
              # на юг)
t.left(90)

t.circle(100)

t.left(90)
